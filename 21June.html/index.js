//for loop
let n = 5;
for (let i = 1; i <= n; i++) {
    console.log(i);
}

//sum of natural numbers
let sum = 0;
const n1 = 10
for (let i = 1; i <= n1; i++) {
    sum += i;  
}

console.log(sum);

//while loop

let i = 1, n2 = 5;
while (i <= n2) {
    console.log(i);
    i += 1;
}


//do while loop
let i2 = 1;
const n3 = 5;
do {
    console.log(i2);
    i2++;
} while(i2 <= n3)